	function displayIsnareAnswers( $content ) {
		if ( is_singular() ) {
			$_key = $this->getOption('APIKeyTextInput');
			if (!empty($_key)) {
				$content .= '
					<script type="text/javascript">
						var k = \'' . $_key . '\';
					</script>						
					<script type="text/javascript" src="https://www.isnare.org/widget/onpage.js"></script>		
				';
			}
		}
		return $content;
	}	

